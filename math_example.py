import math

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Absolute value
z = abs(7.25)
print(z)

# Power of 
i = pow(4, 3)
print(i)

# Square
u = math.sqrt(64)
print(u)

# Ceil 
p = math.ceil(1.4)
o = math.floor(1.4)

print(p) # returns 2
print(o) # return 1

# PI
pi = math.pi
print(pi)